#include <stdio.h>
#include <math.h>

int main() {
	double input[8][8];
	double dct[8][8];
	
	for(int  i = 0; i < 8; i++) {
		for(int j = 0; j < 8; j++) {
			scanf("%lf", &input[i][j]);
		}
	}
	
	for(int  u = 0; u < 8; u++) {
		for(int v = 0; v < 8; v++) {
			double cu = 0, cv = 0;
			double value = 0;
			cu = (u == 0) ? 1/sqrt(2) : 1;
			cv = (v == 0) ? 1/sqrt(2) : 1;
			
			for(int  x = 0; x < 8; x++) {
				for(int y = 0; y < 8; y++) {
					value += input[x][y]*cos(3.141592*(2*x + 1)*u/16)*cos(3.141592*(2*y + 1)*v/16);
				}
			}
			
			dct[u][v] = 0.25*cu*cv*value;
			
		}
	}
	
	for(int  i = 0; i < 8; i++) {
		for(int j = 0; j < 8; j++) {
			printf("%0.2lf ", dct[i][j]);
		}
		printf("\n");
	}
		
	return 0;
}



